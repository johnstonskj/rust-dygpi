#[no_mangle]
pub extern "C" fn register_plugins(registrar: &mut PluginRegistrar<MyPlugin>) {
    registrar.register(MyPlugin::new(PLUGIN_NAME));
}

// ------------------------------------------------------------------------------------------------
// Implementations
// ------------------------------------------------------------------------------------------------

const PLUGIN_NAME: &str = concat!(
    env!("CARGO_PKG_NAME"),
    "::",
    module_path!(),
    "::",
    "MyPlugin"
);

